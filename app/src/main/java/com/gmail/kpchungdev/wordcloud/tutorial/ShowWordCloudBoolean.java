package com.gmail.kpchungdev.wordcloud.tutorial;

public class ShowWordCloudBoolean {

    private boolean showWordCloud;

    public ShowWordCloudBoolean(boolean showWordCloud) {
        this.showWordCloud = showWordCloud;
    }

    public boolean isShowWordCloud() {
        return showWordCloud;
    }

    public void setShowWordCloud(boolean showWordCloud) {
        this.showWordCloud = showWordCloud;
    }

}
